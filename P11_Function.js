// Function Declaration
function greet(name) {
    return `Hello, ${name}!`;
}

// Calling the Function
console.log(greet("Danish")); // Output: Hello, Danish!

// Function Expression
const add = function (a, b) {
    return a + b;
};

// Calling the Function
console.log(add(5, 3)); // Output: 8

// Arrow Function
const multiply = (a, b) => a * b;

// Calling the Function
console.log(multiply(4, 2)); // Output: 8
