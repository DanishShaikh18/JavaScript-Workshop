console.log("For Loop Example:");
// For Loop: Prints numbers from 1 to 5
for (let i = 1; i <= 5; i++) {
    console.log(`For Loop Iteration: ${i}`);
}

console.log("\nWhile Loop Example:");
// While Loop: Prints numbers from 1 to 5
let counter = 1;
while (counter <= 5) {
    console.log(`While Loop Iteration: ${counter}`);
    counter++;
}

console.log("\nDo-While Loop Example:");
// Do-While Loop: Prints numbers from 1 to 5
let doCounter = 1;
do {
    console.log(`Do-While Loop Iteration: ${doCounter}`);
    doCounter++;
} while (doCounter <= 5);
